            $selectQuery = 'SELECT tbl_rodovias_sub_0.idRodoviaSub, tbl_rodovias_sub_0.ano_contratual, tbl_concessionarias_0.idConcessiona AS \'id_concessionaria\', tbl_rodovias_0.idRodovia, tbl_rodovias_0.CodRodovia, tbl_rodovias_sub_0.lote, tbl_concessionarias_0.NomeConcessionaria, tbl_ano_0.ano, Count(tbl_rodovias_sub_0.idRodoviaSub) AS \'id\'
            FROM conspin.tbl_ano tbl_ano_0, conspin.tbl_concessionarias tbl_concessionarias_0, conspin.tbl_rodovias tbl_rodovias_0, conspin.tbl_rodovias_sub tbl_rodovias_sub_0
            WHERE tbl_concessionarias_0.idConcessiona = tbl_rodovias_0.id_concessionaria AND tbl_rodovias_sub_0.ano_contratual = tbl_ano_0.idtbl_ano AND tbl_rodovias_0.idRodovia = tbl_rodovias_sub_0.idRodovia
            GROUP BY tbl_rodovias_sub_0.lote, tbl_ano_0.ano';
            $insertQuery = array('INSERT INTO tbl_rodovias_sub
            VALUES (
            
            :ano_contratual,
            :id_concessionaria)');
            $updateQuery = array();
            $deleteQuery = array();
            $this->dataset = new QueryDataset(
              MyPDOConnectionFactory::getInstance(), 
              GetConnectionOptions(),
              $selectQuery, $insertQuery, $updateQuery, $deleteQuery, 'ano_contratual');
